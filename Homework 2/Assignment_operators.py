# Операторы присваивания в Python (Assignment Operators)

# =  (обычное присваивание)
x = 5                      # x становится 5
print("=  :", x)

# += (прибавить и присвоить)
x = 5
x += 3                     # то же самое что x = x + 3
print("+= :", x)

# -= (вычесть и присвоить)
x = 5
x -= 3                     # то же самое что x = x - 3
print("-= :", x)

# *= (умножить и присвоить)
x = 5
x *= 3                     # то же самое что x = x * 3
print("*= :", x)

# /= (разделить и присвоить)  -> результат будет float
x = 5
x /= 3                     # то же самое что x = x / 3
print("/= :", x)

# %= (остаток от деления и присвоить)
x = 5
x %= 3                     # то же самое что x = x % 3
print("%= :", x)

# //= (целочисленное деление и присвоить)
x = 5
x //= 3                    # то же самое что x = x // 3
print("//=:", x)

# **= (возведение в степень и присвоить)
x = 5
x **= 3                    # то же самое что x = x ** 3
print("**=:", x)

# &= (побитовое AND и присвоить)
x = 5                      # 5 в двоичном: 0101
x &= 3                     # 3 в двоичном: 0011 -> результат 0001 (1)
print("&= :", x)

# |= (побитовое OR и присвоить)
x = 5                      # 0101
x |= 3                     # 0011 -> результат 0111 (7)
print("|= :", x)

# ^= (побитовое XOR и присвоить)
x = 5                      # 0101
x ^= 3                     # 0011 -> результат 0110 (6)
print("^= :", x)

# >>= (сдвиг вправо и присвоить)
x = 8                      # 8: 1000
x >>= 2                    # сдвиг вправо на 2 -> 0010 (2)
print(">>=:", x)

# <<= (сдвиг влево и присвоить)
x = 2                      # 2: 0010
x <<= 3                    # сдвиг влево на 3 -> 10000 (16)
print("<<=:", x)

# := (моржовый оператор) присваивание внутри выражения
# присваиваем значение и сразу используем его в print
print(":= :", (x := 3))    # x получает 3 и печатается 3
print("x  :", x)           # проверка: x теперь 3
