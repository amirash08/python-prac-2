txt = "We are the so-called \"Vikings\" from the north."
#backslash followed by character that is not a letter or a digit, will be included in the string.
txt = "This will insert one \\ (backslash)."
print(txt)
#new line
txt = "Hello\nWorld!"
print(txt)
#carriage return
txt = "Hello\rWorld!"
print(txt)
#tab
txt = "Hello\tWorld!"
print(txt)
#backspace
txt = "Hello\bWorld!"
print(txt)
#form feed
txt = "Hello\fWorld!"
print(txt)
#octal value
txt = "Hello\110\145\154\154\157"
print(txt)
#hex value
txt = "Hello\x48\x65\x6c\x6c\x6f"
print(txt)
